/********************************************************

						继电器通断实验
						
		连接方法：将继电器旁排针RLJP2用跳线帽短路		

********************************************************/


#include <reg52.h>

sbit RELAY = P1^1;			//定义控制继电器为P1^1口

/***********************************************

	   延时函数    延时时间大致为100毫秒

************************************************/
void delay()
{
	int i, j;
	for(i = 0; i < 100; i++)
	{
		for(j = 0; j < 110; j++) ;
	}
}


/***********************************************

	  主函数

***********************************************/
void main()
{
	while(1)
	{
		RELAY = 1;		//继电器连通
		delay();		//延时
		RELAY = 0;		//继电器断开
		delay();		//延时
	}	
}



