/********************************************************

						LED闪烁实验
						
		连接方法：P1口排针和LED排针用排线连接起来
		

********************************************************/


#include <reg52.h>

sbit LED = P1^0;

void delay()
{
	int i,j;
	for(i = 0; i < 100; i++)
	{
		for(j = 0; j < 110; j++) ;
	}
}

void main()
{
	while(1)
	{
		LED = 0;	//点亮LED
		delay();	//延时
		LED = 1;	//熄灭LED
		delay();	//延时
	}
}